package interfaces;

/**
 *
 * @author Carlos
 */
public class Cuadrado implements IFigura {
    double lado;

    public Cuadrado(double lado) {
        this.lado = lado;
    }

    public double getLado() {
        return lado;
    }

    public void setLado(double lado) {
        this.lado = lado;
    }

    @Override
    public String toString() {
        return "Cuadrado{" + "lado=" + lado + '}';
    }

    @Override
    public double calcularArea() {
        return lado*lado;
    }
    
    
    
}
